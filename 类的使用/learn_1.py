#========私有成员与公有成员========================================================
# 
#----------------------------------------------------------------
# 1. _xxx ：保护成员，不能用"from module import *"导入，只有类对象和子对象能访问这些成员
# 2. __xxx__ : 系统定义的特殊成员
# 3. __xxx : 类中的私有成员，只有类对象自己能访问，子类对象也不能访问到这个成员，但在对象外部可以通过"对象名._类名__xxx"来访问
#----------------------------------------------------------------
class Fruit:
	def __init__(self):
		self.__color ='Red'
		self.price = 1

apple = Fruit()
print(apple.price)

