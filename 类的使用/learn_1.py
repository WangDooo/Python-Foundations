# isinstance() 测试一个对象是否为某个类的实例 
# pass 空位
# self参数
#----------------------------------------------------------------
class Car: # 新式类必须至少有一个基类
	def infor(self):
		print("This is a car")
# 定义好之后 实例化对象 "对象名.成员"来访问
car = Car()
car.infor()

print(isinstance(car, Car))

